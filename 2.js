// Cоздать функцию greeting, которая принимает в качестве аргумента
//  имя человека и выводит приветствие, используя переданное ей имя,
//   в консоль.

// Необходимо у пользователя запросить имя и вызвать функцию 
// greeting, передав туда полученное от пользователя значение.

function greeting() {
    const name = prompt('Назовите свое Имя');
    ivent = `Привет, ${name}.`
    alert(ivent);
}
greeting();
console.log(ivent);